"""
q146:  The digraph written as #! is used to:


Answer : tell a Unix or Unix-like OS how to execute
the contents of a Python file.


----------------
Use shebang lines: A shebang line is a
special line at the beginning of a script
that specifies the interpreter to use.



A shebang line of #!/bin/bash tells the
system to use the Bash interpreter
to run the script.

#!/bin/bash

echo("Hello, World!")


------------------------


A shebang line of #!/usr/bin/python3 tells
 the system to use the Python interpreter
  to run the script.

#!/usr/bin/python3

print("Hello, World!")
"""