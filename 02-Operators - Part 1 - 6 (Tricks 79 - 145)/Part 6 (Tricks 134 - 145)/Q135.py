"""
Identity operators are used to compare the objects,
not if they are equal, but if they are actually the
same object, with the same memory location:

is      : Returns True if both variables are the same object

is not	: Returns True if both variables are not the same object

Membership operators are used to test if a sequence is
presented in an object:

in 	    : Returns True if a sequence with the specified value
is present in the object

not in	: Returns True if a sequence with the specified value
is not present in the object
"""


print('is' in 'This ISPython code. ') #True

print('t' in 'Peter') # True

